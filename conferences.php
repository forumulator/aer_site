                                            <div style="display: none;" id="pio_oci" class="tabbed-intf media-body">
                                                <div class="media-heading">
                                                    <a style="" href="#">What is an international conference?</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">International
 Conferences/Seminars/Workshops (herein after referred to as events) are
 those events where substantive discussions/deliberations/interaction 
and exchange of thoughts and ideas will take place on a specific subject
 matter and in which participants from foreign countries will take part.  </span>
<br>
<div style="display: block; position: relative; float: right; color: Blue;">More...</div>
                                                </div>
<br>

<div class="media-heading">
                                                    <a style="" href="#">What does not constitute an international conference?</a>
                                                </div>

<div style="color: white;" class="muted">
                                                    <ul style="color: black; font-size: 12px;">
  <li>Meetings organized by any business institution having foreign 
participants from its sister concern abroad, and solely on the subject 
matter with the business issues of that business institution.</li><li>Sports
 meet and sports events organized by recognized sports bodies of India 
where sports-persons of foreign countries will take part.</li><li>Events
 of performing arts such as Drama Festivals, Mussaira, Kavi Sammelan 
etc. in which groups from foreign countries will take part.</li><li>Meet
 of businessmen/industrialists from India and abroad organized by Indian
 Chambers of Commerce where trade and business issues will be discussed.</li>
</ul>
<br>

                                                </div>

                                            </div>
<div style="display: none;" id="self_fin" class="tabbed-intf media-body">
                                                <div class="media-heading">
                                                    <a style="" href="#">Self financed candidates</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">
 
  The organizers conceiving an idea of holding an event in India will be
 required to submit proposal which must contain information like:-
  <ul> 
    <li> Name of the organizer with complete mailing address. </li>
    <li> Topic/Topics to be covered in the event. </li>
    <li> Venue of the event. </li>
    <li> Date of the event. </li>
    <li> Tentative lists of the participating countries and participants
 with nationality, brief background suiting participation in the event, 
address, personal and passport details. </li>
  </ul>
Proposal should invariably be submitted to the nodal ministry and not to
 the Ministry of Home Affairs. The nodal Ministry will give clearance to
 the event. The nodal Ministry will obtain clearance of Home Ministry or
 other agencies, if necessary within the time frame (8 weeks). For 
organizers it will be a single window clearance.
</span>
<span style="display: block; position: relative; float: right; color: Blue;">More...</span>
                                                </div>
                                            </div>
<div style="display: none;" id="info_inc" class="tabbed-intf media-body">
                                                <div class="media-heading">
                                                    <a style="" href="#">What is nodal/administrative ministry?</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">
   
The nodal/administrative Ministry means the Ministry of Govt. of India 
which is dealing/regulating framing rules etc. in respect of subject 
matter chosen for the event. For example, if an organizer is proposing 
to hold an event on the topic of 'Adult Non-formal education', then, the
 nodal/administrative ministry shall be Ministry of HRD, D/o Education. 
Suppose an organizer proposes to hold an event on topic related to 
empowerment of women in India then the nodal ministry shall be Ministry 
of Social Welfare, Deptt. of Women and Child Development etc.

  </span>
<span style="display: block; position: relative; float: right; color: Blue;">More...</span>
                                                </div>

<br>
<div class="media-heading">
                                                    <a style="" href="#">Time frame to be followed </a>
                                                </div>

<div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">Organizers
 are required to submit their proposal to administrative/nodal ministry 
at least 8 weeks before the commencement of the event.</span>
<span style="display: block; position: relative; float: right; color: Blue;">More...</span>
                                                </div>
                                            </div>
<div style="display: none;" id="info_int" class="media-body tabbed-intf">
                                                <div class="media-heading">
                                                    <a style="" href="#">VISA to the foreign participants</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">Once
 an event is cleared by nodal Ministry, organizers may send invitation 
to foreigners who wish to participate in the event. Foreigners should 
invariably be advised to opt for 'conference visa' only. [In the 
Invitation letter, the organizers should mention the details of 
clearance accorded by nodal Ministry for conducting the event]. With 
invitation letter the participants should approach Missions abroad for 
grant of conference visa.<br> <br> </span>

                                                </div>

<div class="media-heading">
                                                    <a style="" href="#">In case of difficulty in getting visa</a>
                                                </div>

<div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">Some
 of the participants may face problem in obtaining conference visa. In 
these cases, organizers should immediately obtain and furnish the 
following particulars of the foreigners to nodal ministry with specific 
request for visa:-<br> <br>
<ul>
  <li> Sl. No.</li>
  <li> Name of the participant</li>
  <li> Father/Husband's Name</li>
  <li> Nationality</li>
  <li> Date of Birth</li>
  <li> Place of Birth</li>
  <li> Passport Number</li>
  <li> Date and place of issue of passport</li>
  <li> Date of expiry of passport</li>
  <li> Address</li>
</ul> </span>
<span style="display: block; position: relative; float: right; color: Blue;">More...</span>
                                                </div>
                                            </div>
<div style="display: none;" id="bef_iitg" class="media-body tabbed-intf">
                                                <div class="media-heading">
                                                    <a style="" href="#">Onus of obtaining clearence for an event</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">
  Organizers need not request Ministry of External Affairs and Ministry 
of Home Affairs directly for clearance. On receipt of the proposal from 
the organizers, nodal/administrative ministry will examine the proposal 
and if they feel that clearance of MHA and MEA is required the same 
shall be obtained by them and they in turn grant overall clearance to 
organizers.
  <br>
  <br>
  Onus of obtaining clearance of MHA &amp; MEA would rest with nodal/administrative ministries and not with the organizer.
</span>

                                                </div>
                                            </div>
<div style="display: block;" id="aft_iitg" class="media-body tabbed-intf">
                                                <div class="media-heading">
                                                    <a style="" href="#">Steps to be taken for clearence</a>
                                                </div>
                                                <div style="color: white;" class="muted">
                                                    <span style="color: black;" class="date">
  Organizers are advised to go through the streamlined channel of 
submission of proposals for the events and adhere to the time frame (at 
least 8 weeks ahead of event). If the above procedure is followed, there
 will not be any difficulty in getting clearance of event in time.
  </span>

                                                </div>
